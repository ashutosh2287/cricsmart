export function translateCommentary(
  text: string,
  lang: "EN" | "HI"
): string {

  if (lang === "EN") return text;

  function pick(arr: string[]) {
    return arr[Math.floor(Math.random() * arr.length)];
  }

  /* =============================
     🔴 WICKET
  ============================= */
  if (text.includes("WICKET") || text.includes("OUT")) {
    return pick([
      "आउट! बल्लेबाज़ को पवेलियन लौटना होगा!",
      "विकेट! शानदार गेंदबाज़ी, बड़ा झटका!",
      "ओह! बल्लेबाज़ आउट हो गए, मैच का रुख बदल सकता है!",
      "ये बहुत बड़ा विकेट है!",
    ]);
  }

  /* =============================
     🟣 SIX
  ============================= */
  if (text.includes("SIX")) {
    return pick([
      "छक्का! गेंद सीधे दर्शकों के बीच!",
      "क्या शॉट है! शानदार छक्का!",
      "गेंद हवा में... और ये गया सीमा रेखा के पार!",
      "ज़बरदस्त प्रहार! पूरा छक्का!",
    ]);
  }

  /* =============================
     🟢 FOUR
  ============================= */
  if (text.includes("FOUR")) {
    return pick([
      "चौका! शानदार टाइमिंग!",
      "बेहतरीन शॉट, गेंद सीमा रेखा के पार!",
      "गैप में खेला और चार रन मिल गए!",
      "तेज़ शॉट! फील्डर के पास कोई मौका नहीं!",
    ]);
  }

  /* =============================
     ⚪ DOT BALL
  ============================= */
  if (text.includes("Dot") || text.includes("No run")) {
    return pick([
      "कोई रन नहीं, बढ़िया गेंदबाज़ी।",
      "डॉट बॉल! दबाव बढ़ता हुआ।",
      "सिर्फ डिफेंस, रन नहीं मिला।",
      "अच्छी लाइन और लेंथ, रन नहीं।",
    ]);
  }

  /* =============================
     🏃 RUNS
  ============================= */
  if (text.includes("1")) {
    return pick([
      "एक रन लिया, स्ट्राइक बदल दी।",
      "सिंगल लेकर खेल आगे बढ़ाया।",
    ]);
  }

  if (text.includes("2")) {
    return pick([
      "दो रन के लिए बढ़िया दौड़।",
      "अच्छी रनिंग, दो रन मिल गए।",
    ]);
  }

  if (text.includes("3")) {
    return pick([
      "तीन रन! शानदार दौड़।",
      "तेज़ी से दौड़कर तीन रन पूरे किए।",
    ]);
  }

  /* =============================
     DEFAULT
  ============================= */

  return "सामान्य खेल जारी है।";
}